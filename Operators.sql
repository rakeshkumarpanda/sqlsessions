--5.SET
--A. UNION - RETURNS VALUES FROM BOTH THE TABLE WITHOUT DUPLICATES
--B. UNION ALL - RETURNS VALUES FROM BOTH THE TABLE WITH DUPLICATES
--C. INTERSECT - RETURNS THE COMMON VALUES FROM BOTH THE TABLE 
--D. MINUS - RETURNS VALUES FROM THE FIRST TABLE WHICH ARE NOT PRESENT IN SECOND TABLE
SELECT * FROM STUDENT, LIB;
SELECT ID, SNAME FROM STUDENT UNION SELECT ID, SNAME FROM LIB;
SELECT ID, SNAME FROM STUDENT UNION ALL SELECT ID, SNAME FROM LIB;
SELECT ID, SNAME FROM STUDENT INTERSECT SELECT ID, SNAME FROM LIB;
SELECT ID, SNAME FROM STUDENT MINUS SELECT ID, SNAME FROM LIB;
SELECT ID, SNAME FROM LIB MINUS SELECT ID, SNAME FROM STUDENT;